
public class Application {

	public static void main(String[] args) {
		int myNumber = 88; //32 bit value
        short myShort = 847; //16 bit value
        long myLong = 9797; //64 bit value
        
        double myDouble = 7.3243; //There is no long version of double
        float myFloat = 324.3f; //You have to put f at the end of it to make it float.
        
        
        char myChar = 'y';
        boolean myBoolean = false;
        
        byte myByte = 127; //holds 8 bit of data
        
        
        System.out.println(myNumber);
        System.out.println(myShort);
        System.out.println(myLong);
        System.out.println(myDouble);
        System.out.println(myFloat);
        System.out.println(myChar);
        System.out.println(myBoolean);
        System.out.println(myByte);
	}

}
