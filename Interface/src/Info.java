// We create interface whenever we see that there are two 
// or more classes that will have same method
// implementing class has to implement all of the methods of interface
/*
 * Class can implements more than one interfaces.
 * Class can only extends one superclass.
 * You can use interface everywhere else except while
 * creating new object (You have to pass class name.)
 */

public interface Info {
	// interface does not have method code
	public void showInfo();

}
